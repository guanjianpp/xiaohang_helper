def load_school_info():
    files = ["01_新生入学.md", "02_办事流程.md", "03_电话黄页.md", "04_应急防骗.md","05_交通出行.md"]
    data_dir = "data"
    content = ""
    for fname in files:
        path = f"{data_dir}/{fname}"
        try:
            with open(path, "r", encoding="utf-8") as f:
                content += f"\n\n=== {fname} ===\n" + f.read()
        except FileNotFoundError:
            print(f"⚠ 文件不存在：{path}")
    return content

